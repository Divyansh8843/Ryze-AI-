const axios = require('axios');

/**
 * Export generated UI code to a new GitHub repository.
 *
 * This is a demo-style implementation aimed at showcasing "export to GitHub"
 * capability for the assignment. It expects a short‑lived Personal Access Token
 * that the user pastes into the UI (with `repo` scope).
 *
 * Body: { token, repoName, code }
 */
exports.exportToGitHub = async (req, res) => {
  const { token, repoName, code } = req.body;

  if (!token || !repoName || !code) {
    return res.status(400).json({
      error: 'token, repoName and code are required for GitHub export.',
    });
  }

  try {
    const api = axios.create({
      baseURL: 'https://api.github.com',
      headers: {
        Authorization: `Bearer ${token}`,
        Accept: 'application/vnd.github+json',
      },
    });

    // 1) Get current user (to know owner login)
    const userResp = await api.get('/user');
    const owner = userResp.data.login;

    // 2) Create a new repository (private by default for safety)
    const repoResp = await api.post('/user/repos', {
      name: repoName,
      description: 'Exported from Ryze AI Deterministic UI Generator',
      private: true,
    });

    const repoFullName = repoResp.data.full_name; // owner/repo
    const htmlUrl = repoResp.data.html_url;
    const cloneUrl = repoResp.data.clone_url;

    // 3) Create a single file with the generated UI code
    const filePath = 'src/GeneratedPage.jsx';
    const content = Buffer.from(code, 'utf8').toString('base64');

    await api.put(`/repos/${repoFullName}/contents/${encodeURIComponent(filePath)}`, {
      message: 'Add generated UI component',
      content,
    });

    // 4) Also add a minimal README to explain what this repo is
    const readmeContent = Buffer.from(
      [
        '# Ryze AI – Exported UI',
        '',
        'This repository was created automatically from the Ryze AI Deterministic UI Generator.',
        '',
        'The main generated component lives in `src/GeneratedPage.jsx`.',
      ].join('\n'),
      'utf8'
    ).toString('base64');

    await api.put(`/repos/${repoFullName}/contents/README.md`, {
      message: 'Add autogenerated README',
      content: readmeContent,
    });

    return res.json({
      success: true,
      repoFullName,
      htmlUrl,
      cloneUrl,
    });
  } catch (err) {
    console.error('[GitHub Export] Failed to export repository', err?.response?.data || err.message);
    const status = err?.response?.status || 500;
    const message =
      status === 401
        ? 'GitHub authentication failed. Please check that your token is valid and has the `repo` scope.'
        : 'Failed to export to GitHub. See server logs for details.';

    return res.status(status).json({ error: message });
  }
};

